def main():
    colors = {
        1: "Червоний",
        2: "Помаранчевий",
        3: "Жовтий",
        4: "Зелений",
        5: "Блакитний",
        6: "Синій",
        7: "Фіолетовий"
    }
    
    number = int(input("Введіть порядковий номер кольору (1-7): "))
    
    if number in colors:
        print(f"Колір з порядковим номером {number} - {colors[number]}")
    else:
        print("Немає такого кольору в спектрі.")

if __name__ == "__main__":
    main()
